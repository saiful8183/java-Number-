package numberPractice;

public class EvenOrOdd {

	public static void main(String[] args) {
		
		
		
		int number = 6;
			


		// Find out if the number above is even or odd
		if (number % 2 == 0) {
		  System.out.println(number + " is even.");
		} else {
		  System.out.println(number + " is odd.");
		}
		
		
		
		
		
		
		
		
		
		
	}

}
